function Country2Code($countryname) {
    global $country, $country_alias;
        
    // make all seaches lower case
    $countryname = strtolower($countryname);
    
    // check if the country name is in the alias list
    if (isset($country_alias[$countryname]))
        return $country_alias[$countryname];

    // if not, scan though the country name table        
    foreach ($country as $key => $value) {
        if (strtolower($value) == $countryname) {
            return $key;
        }
    }
    // not found?
    return "??";
}

