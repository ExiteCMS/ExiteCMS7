			if ($step == "install") {
				if (!CMS_CLI) $variables['message'] .= sprintf($locale['306'],LP_LOCALE, LP_LANGUAGE);
			} else {
				if (!CMS_CLI) $variables['message'] .= sprintf($locale['307'],LP_LOCALE, LP_LANGUAGE);
